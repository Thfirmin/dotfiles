const char * op_to_name(int op)
{
  size_t i;

  for(i = 0; i < sizeof operators / sizeof *operators; ++i)
    if(operators[i].value == op)
      return operators[i].name;
  return NULL;
}

int	main(void)
{
	char	*err_message = 0;
	for (register short i = 0; i <= 133; i ++)
	{
		err_message = strerror(i);
		if (i < 10)	
			printf("errno[%d] \t| %s | %s\n", i, op_to_name(i), err_message);
		else
			printf("errno[%d] \t| %s | %s\n", i, op_to_name(i), err_message);
		printf ("----------------------------------------------------------\n");
	}
	return (0);
}

